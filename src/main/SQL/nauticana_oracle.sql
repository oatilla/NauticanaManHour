CREATE OR REPLACE VIEW USER_MENU_PERMISSION AS
SELECT DISTINCT
       U.USERNAME,
       P.PAGENAME,
       P.CAPTION AS PAGE_CAPTION,
       P.ICON AS PAGE_ICON,
       P.URL,
       M.MENU,
       M.CAPTION AS MENU_CAPTION,
       M.ICON AS MENU_ICON,
       M.DISPLAY_ORDER AS MENU_ORDER,
       P.DISPLAY_ORDER AS PAGE_ORDER
  FROM OBJECT_AUTHORIZATION T, USER_AUTHORIZATION U, SCREEN_PAGE P, MAIN_MENU M
 WHERE T.AUTHORITY_GROUP=U.AUTHORITY_GROUP
   AND T.OBJECT_TYPE='PAGE'
   AND T.ACTION='ACCESS'
   AND T.KEY_VALUE=P.PAGENAME
   AND M.MENU=P.MENU;

commit;

create or replace view captions_to_translate AS
select caption from caption_translation
union
select table_name from user_tables
union
select column_name from user_tab_columns
union
select action from authority_object_action
UNION
select object_type from authority_object
UNION
SELECT CAPTION FROM AUTHORITY_GROUP
UNION
SELECT CAPTION FROM DOMAIN_NAME
UNION
SELECT CAPTION FROM DOMAIN_VALUE
UNION
SELECT CAPTION FROM LANGUAGE
UNION
SELECT CAPTION FROM MAIN_MENU
UNION
SELECT CAPTION FROM SCREEN_PAGE;

create or replace view all_translations as
select distinct c.caption,
       e.labelupper label_upper_en, e.labellower  label_lower_en,
       t.labelupper label_upper_tr, t.labellower  label_lower_tr,
       r.labelupper label_upper_ru, r.labellower  label_lower_ru,
       a.labelupper label_upper_ar, a.labellower  label_lower_ar
  from captions_to_translate c, caption_translation e, caption_translation t, caption_translation r, caption_translation a
 where e.langcode(+)='EN'
   and e.caption(+)=c.caption
   and t.langcode(+)='TR'
   and t.caption(+)=c.caption
   and r.langcode(+)='RU'
   and r.caption(+)=c.caption
   and a.langcode(+)='AR'
   and a.caption(+)=c.caption

spool off
